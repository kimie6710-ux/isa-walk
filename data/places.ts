export const getRoute = (type: string | undefined | null): Route => {
  if (type && type in routes) return routes[type as RouteType];
  return routes.relax;
};

export const isRouteType = (v: unknown): v is RouteType =>
  typeof v === 'string' && v in routes;

export const routeTotalMin = (route: Route): number =>
  route.places.reduce(
    (sum, p) => sum + p.stayMin + (p.walkFromMin ?? 0),
    0,
  );
